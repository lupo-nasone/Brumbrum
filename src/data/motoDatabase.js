// Funzione per ottenere le statistiche di una moto specifica
export function getMotoStats(marca, modello) {
  const marcaLower = marca.toLowerCase();
  const modelloKey = modello.toLowerCase().replace(/[\s-]/g, '').replace('v', 'v');
  
  if (motoDatabase[marcaLower] && motoDatabase[marcaLower][modelloKey]) {
    return motoDatabase[marcaLower][modelloKey];
  }
  
  return null;
}

// Funzione per ottenere solo le statistiche principali
export function getMotoMainStats(marca, modello) {
  const stats = getMotoStats(marca, modello);
  if (!stats) return null;
  
  return {
    nome: stats.nome,
    anno: stats.anno,
    potenza: stats.potenza,
    cilindrata: stats.cilindrata,
    peso: stats.peso,
    velocitaMax: stats.velocitaMax
  };
}